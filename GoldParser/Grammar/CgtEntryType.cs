namespace bsn.GoldParser.Grammar {
	/// <summary>
	/// Entry type byte in the binary grammar file.
	/// </summary>
	internal enum CgtEntryType {
		Empty = (int)'E', // 69
		Integer = (int)'I', // 73
		String = (int)'S', // 83
		Boolean = (int)'B', // 66
		Byte = (int)'b' // 98
	}
}
